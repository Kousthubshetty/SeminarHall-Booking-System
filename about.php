            <h2> <span>Seminar</span>Hall Booking</h2>
              
              <p> Adding to the innumerous facilities of SDMC the Samyakdarshan hall or the Seminar hall is one of the 'so-very-busy' rooms of the college that after fixing  a programme what the organizers worry about is nothing but the availability of Seminar hall. This  well equipped hall is a pride of SDMC.
It, when started 10 years ago, was used by the SDM sister institutions and other colleges of the locality as well, since it was the only hall with modern facilities in this area. At present the Seminar hall has LCD projectors, OHP projectors, Slide projector, Mike, Audio recording and other facilities. It has the capacity of holding 200 people which makes it a perfect venue for guest lectures, seminars, power point presentations,  interclass competitions, and other cultural events.
This Seminar hall has witnessed one international seminar, more than a hundred UGC/national level events and hundreds of state level programmes. The hall serves as a venue for the important meetings of the college like BOS meeting and so on. The rotary programmes, JCI meetings take place here. It is busy throughout the year because in the vacations too the answer paper evaluation process, admission process keep the hall engaged.  It is also a place for bus pass distribution, uniform distribution and other related affairs. The vast room behind the seminar hall is used to make food arrangements for the guests and it is also a rest room.
The gentlemen  who work behind  the screens of the seminar hall are Mr. Sandeep and Mr. Surendra. They make arrangements for the programmes , stage setting  and they also keep records of the events which are held and also of the upcoming events. "Per day the seminar hall will be engaged at least for 5 hours", says Mr. Sandeep, "and the experience of dealing with these many programmes has rendered me quickness and expertise in this work." He has been working here since the beginning of the Seminar hall. Mr. Surendra who has been working here for the past 5 years manages the photography section. Recording the events, bookings, making food arrangements for the guests, decoration of the hall etc come under the responsibility of these men. Both of them are very cooperative, and committed to their duties.  To book the seminar hall the organizers should approach Mr. Sandeep at least one week before the event. In case of national level events the organizers are expected to consult him a month earlier.
It is often noted that on finding the seminar hall unavailable for their programme, the organizers turn towards the audio visual halls or any other alternatives. But the Seminar hall is everybody's first preference and this makes it the most desirable venue for the events of SDMC.

